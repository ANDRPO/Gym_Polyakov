package com.example.gym_polyakov.ui;

public class Get_Set_Day {
    private int number;
    private String day;

    public Get_Set_Day(int number, String day) {
        this.number = number;
        this.day = day;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }
}
