package com.example.gym_polyakov;

public class Graph_DATA {
    int number;
    int date;

    public Graph_DATA(int number, int date) {
        this.number = number;
        this.date = date;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public int getDate() {
        return date;
    }

    public void setDate(int date) {
        this.date = date;
    }
}
